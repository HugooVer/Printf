#include "ft_printf.h"

void	ft_printX(unsigned int X)
{
	char	*str;

	str = "0123456789ABCDEF";
	if (X > 0)
	{
		ft_printX(X / 16);
		write(1, &str[X % 16], 1);
	}
}

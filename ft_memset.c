#include "ft_printf.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (n > i)
	{
		((unsigned char *)s)[n - i - 1] = (unsigned char)c;
		++i;
	}
	return (s);
}
